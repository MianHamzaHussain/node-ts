function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
var cityAndCountry1 = city_country("Karachi", "Pakistan");
console.log(cityAndCountry1);
var cityAndCountry2 = city_country("Tokyo", "Japan");
console.log(cityAndCountry2);
var cityAndCountry3 = city_country("New York", "USA");
console.log(cityAndCountry3);
